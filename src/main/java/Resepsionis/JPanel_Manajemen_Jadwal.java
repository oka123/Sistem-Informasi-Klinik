    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        // 1. Dapatkan baris yang dipilih
        int selectedRow = tblJadwal.getSelectedRow();

        // 2. Cek apakah ada baris yang dipilih
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, 
                    "Pilih data jadwal yang ingin Anda hapus.", 
                    "Peringatan", 
                    JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 3. Ambil ID dan Nama untuk konfirmasi
        String idJadwal = tblJadwal.getValueAt(selectedRow, 0).toString();
        String namaDokter = tblJadwal.getValueAt(selectedRow, 1).toString();

        // 4. Tampilkan dialog konfirmasi
        int pilihan = JOptionPane.showConfirmDialog(this,
                "Apakah Anda yakin ingin menghapus data jadwal:\n" + namaDokter + " (ID: " + idJadwal + ")?",
                "Konfirmasi Hapus Data",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.WARNING_MESSAGE
        );

        // 5. Cek jika pengguna menekan "Ya"
        if (pilihan == JOptionPane.YES_OPTION) {
            // 6. Lakukan operasi database
            String sql = "DELETE FROM jadwal_praktik WHERE jadwal_id = ?";
            
            try (PreparedStatement pstmt = this.conn.prepareStatement(sql)) {

                pstmt.setString(1, idJadwal);

                // 7. Eksekusi delete
                int rowsAffected = pstmt.executeUpdate();

                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(this, 
                            "Data jadwal berhasil dihapus.", 
                            "Sukses", 
                            JOptionPane.INFORMATION_MESSAGE);

                    // 8. Muat ulang data (dan bersihkan pencarian)
                       String filterSaatIni = comboFilterHari.getSelectedItem().toString();
                       loadDataJadwal(filterSaatIni);
                } else {
                    JOptionPane.showMessageDialog(this, 
                            "Data jadwal tidak ditemukan (mungkin sudah dihapus).", 
                            "Info", 
                            JOptionPane.INFORMATION_MESSAGE);
                }


            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(this, 
                        "Gagal menghapus data: " + e.getMessage(), 
                        "Error Database", 
                        JOptionPane.ERROR_MESSAGE);
//                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

